    private void jtable_empMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtable_empMouseClicked

        DefaultTableModel dm = (DefaultTableModel) jtable_emp.getModel();
        int row = jtable_emp.getSelectedRow();
        int col = 0;
        String emp_no = dm.getValueAt(row, col).toString();
        String begin = edit_begin.getText();
        String end = edit_end.getText();

        listClockOn(emp_no, begin, end);
        
        // 取得員工實體照片
        image1.setIcon(new ImageIcon("/Users/vincenttuan/NetBeansProjects/HS_Personnel/photo/" + emp_no + ".png"));
        if(image1.getIcon().getIconHeight() == -1) {
            image1.setText("image1 沒有照片");
        } else {
            image1.setText("");
        }
        image2.setIcon(null);
        image2.setText("image2 沒有照片");
    }//GEN-LAST:event_jtable_empMouseClicked

    private void jtable_clockonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtable_clockonMouseClicked
        DefaultTableModel dm = (DefaultTableModel) jtable_clockon.getModel();
        int row = jtable_clockon.getSelectedRow();
        int col = 0;
        // 取得所選取到的 clock_id
        int clock_id = Integer.parseInt(dm.getValueAt(row, col).toString());
        // 取得 image 字串 (base64)
        String imageBase64 = clockOnDao.getImage(clock_id);
        if(imageBase64 == null) {
            image2.setIcon(null);
            image2.setText("image2 沒有照片");
        } else {
            // 字串(base64) 轉 bufferedImage
            BufferedImage bufferedImage = ImageUtil.base64StringToImage(imageBase64);
            // bufferedImage 轉 ImageIcon 並送入 image2 中來顯示 
            image2.setIcon(new ImageIcon(bufferedImage));
            image2.setText("");
        }
        
    }//GEN-LAST:event_jtable_clockonMouseClicked

