    private void listEmployees() {
        DefaultTableModel dm = (DefaultTableModel) jtable_emp.getModel();
        dm.setRowCount(0);
        List<Map<String, Object>> list = employeeDao.queryAll();

        for (Map<String, Object> map : list) {
            Object[] rowData = {
                map.get("emp_no"),
                map.get("emp_name")
            };
            dm.addRow(rowData);
        }
    }

    private void listClockOn(String emp_no, String begin, String end) {
        DefaultTableModel dm = (DefaultTableModel) jtable_clockon.getModel();
        dm.setRowCount(0); // 清空 jTable
        List<Map<String, String>> list = clockOnDao.query(emp_no, begin, end);
        for (Map<String, String> map : list) {
            Object[] rowData = {
                map.get("clock_id"),
                map.get("emp_no"),
                map.get("emp_name"),
                map.get("status_name"),
                map.get("clock_on")
            };
            dm.addRow(rowData);
        }
    }

